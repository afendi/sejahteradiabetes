    <p class="p-3">
      Penyakit gusi adalah penyakit kronik yang sukar dikesan oleh pesakit pada
      peringkat awal. Antara tanda awal penyakit gusi adalah gusi menjadi
      kemerah-merahan dan mudah berdarah semasa memberus gigi. Ini adalah
      tanda-tanda gingivitis atau tahap awal penyakit gusi. Sekiranya ini
      berlaku, gosok gigi dan gusi anda dengan teliti. Jangan berhenti menggosok
      atau membersihkan kawasan yang merah atau berdarah itu kerana ia akan
      menjadikannya lebih teruk.
    </p>

    <p class="p-3">
      Apabila gingivitis dibiarkan begitu sahaja, ia akan menjadi semakin teruk
      di mana tanda-tanda seperti gusi menjadi bengkak; gigi kelihatan panjang
      kerana gusi telah menyusut dan ruang antara gigi kelihatan besar. Tulang
      yang menyokong gigi akan turut menyusut menjadikan gigi bergoyang dan
      akhirnya pesakit boleh kehilangan gigi sekiranya dibiarkan berlarutan.
      Tahap ini dipanggil periodontitis.
    </p>

    <p class="p-3">
      Selain itu masalah yang paling ketara bagi mereka yang mempunyai penyakit
      gusi adalah halitosis atau masalah nafas berbau. Dan bagi pesakit diabetes
      pula, ini ditambah dengan bau mulut yang unik terutama pada pesakit
      diabetes yang tidak terkawal. Kedua-dua jenis bau nafas ini mengganggu
      kualiti hidup mereka yang menghidapnya dan orang sekeliling. Tetapi, agak
      sukar untuk anda mengenal pasti bahawa nafas anda berbau. Oleh itu,
      pastikan anda sentiasa menjaga kebersihan mulut anda. Berus gigi anda di
      hadapan cermin muka agar anda dapat melihat keadaan gigi dan gusi anda.
      Kunjungi doktor gigi sekurang-kurangnya dua kali setahun untuk pemeriksaan
      berkala atau dengan segera sekiranya anda ada tanda-tanda gingivitis atau
      periodontitis di atas.
    </p>

    <p class="p-3 font-bold">
      Pemeriksaan Mulut Sendiri (Gigi dan Mukosa Mulut)
    </p>

    <p class="p-3">
      Cara-cara Melakukan Pemeriksaan Mulut Sendiri untuk Mengenal pasti Masalah
      Gigi Berlubang dan Lain-lain masalah dalam mulut.
    </p>
    <p class="p-3">
      Kesihatan mulut merupakan aspek yang penting dalam kehidupan. Anda
      digalakkan untuk sentiasa memeriksa keadaan mulut anda iaitu gusi, gigi,
      lidah dan mukosa mulut setiap kali memberus gigi.
    </p>
    <p class="p-3">
      Perhatikan sekiranya ada tompokan warna coklat atau kehitaman pada gigi
      terutamanya di permukaan mengunyah. Ini menandakan gigi tersebut mungkin
      telah mengalami kerosakan. Kerosakan ini boleh dikesan lebih awal oleh
      doktor gigi anda. Malah, sekiranya masih diperingkat awal, kerosakan gigi
      boleh dicegah dan gigi berlubang boleh dielakkan.
    </p>
    <img
      src="https://upload.wikimedia.org/wikipedia/commons/6/66/Toothdecay_%281%29.jpg"
      alt="Toothdecay" />
    <p class="p-3">
      Perhatikan juga sekiranya ada perubahan warna atau sifat pada mukosa mulut
      anda seperti permukaan tepi lidah, lelangit, gusi serta bahagian dalam
      pipi anda. Sekiranya ada lesi atau tompok merah atau putih atau
      kedua-duanya, sila dapatkan pemeriksaan doktor gigi dengan segera.
      Sesetengah lesi ini boleh bertukar menjadi kanser mulut. Pemeriksaan mulut
      oleh doktor gigi penting kerana kadang-kala sukar untuk anda untuk
      mengenalpasti lesi-lesi ini.
    </p>
    <p class="p-3">Contoh lesi putih di lelangit</p>
    <img
      src="https://upload.wikimedia.org/wikipedia/commons/9/97/Orale_Leukoplakie.jpg" width="446px" height="475px"
      alt="Lesi putih" />
    <p class="p-3">Contoh campuran lesi putih dan merah pada mukosa mulut</p>
    <img
      src="https://upload.wikimedia.org/wikipedia/commons/8/80/NIH_DOC_19_Erythroleukoplakia.jpg"
      alt="mukosa" />
    <p class="p-3">
      Selain itu sekiranya ada ulser yang tidak sembuh setelah dua minggu atau
      ada benjolan luar biasa di dalam mulut anda, dapatkan pemeriksaan segera
      dari doktor gigi anda. Pemeriksaan awal dapat meningkatkan kebarangkalian
      untuk sembuh dan dapat mencegah dari penyakit menjadi lebih teruk.
    </p>
